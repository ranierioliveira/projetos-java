public class Variables {
    public static void main(String[] args) throws Exception {
        
        //Variáveis do tipo inteiro
        byte numeroPequeno = 127;
        short numeroShort = 32000;  
        int numeroMaior = 2000000;
        long numeroEnorme = 500000000000l;

        //Variáveis do tipo flutuante
        float peso = 71.5f;
        double pi =  3.1416;

        //Variáveis do tipo caractere
        char letra = 'L';

        //Variável booleana
        boolean estaDeNoite = false;

        //Variáveis do tipo String
        String nome = "Ranieri Pinheiro de Oliveira";

        System.out.println(numeroPequeno); 
        System.out.println(numeroShort);
        System.out.println(numeroMaior);
        System.out.println(numeroEnorme);
        System.out.println(peso);
        System.out.println(pi);
        System.out.println(nome);
        System.out.println(letra);
        System.out.println(estaDeNoite);
    }
}
